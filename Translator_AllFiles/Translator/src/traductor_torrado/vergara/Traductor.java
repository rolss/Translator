    class Nodo{
        String Idiomas;
        Nodo linkIdiomas;
        NodoP linkPalabra;
    }
    
    class NodoP{
        String Palabras;
        NodoP linkPalabra;
    }
    
    Nodo addcola(Nodo ptr, String Idio){
        Nodo p = ptr;
        Nodo q = new Nodo();
        q.Idiomas = Idio;
        if(ptr == null){
            ptr = q;
        }else{
            while(p.linkIdiomas!=null){
                p = p.linkIdiomas;
            }
            p.linkIdiomas = q;
        }
        return ptr;
    }
    Nodo BuscaIdiomas(Nodo ptr, String IdiomaB){
        Nodo p = ptr;
        while(p.linkIdiomas!=null && !p.Idiomas.equalsIgnoreCase(IdiomaB)){
            p = p.linkIdiomas;
        }
        if(!p.Idiomas.equals(IdiomaB)){
            JOptionPane.showMessageDialog(null, "No se encontro el idioma");
        }
        return p;
    }
    
    void BuscaPalabras(String word, String IdiomaB){
        Nodo p = ptr;
        BuscaIdiomas(p, IdiomaB);
    }
    
    

    void TranslateWord(Nodo ptr, String IdiomaO, String IdiomaD, String word){
        Nodo p = ptr;
        int cont=0,i=0;
        while(p != null && !p.Idiomas.equals(IdiomaO)){
            p=p.linkIdiomas;
        }
        if(p!=null){
            if(p.Idiomas.equals(IdiomaO)){
                if(p.linkPalabra != null){
                    NodoP q = p.linkPalabra;
                    while(q != null && !q.Palabras.equals(word)){
                        q = q.linkPalabra;
                        cont=cont+1;
                    }
                    if(q!=null && q.Palabras.equals(word)){
                        Nodo pp = ptr;
                        while(pp != null && !pp.Idiomas.equals(IdiomaD)){
                            pp=pp.linkIdiomas;
                        }
                        if(pp!=null && pp.Idiomas.equals(IdiomaD)){
                            NodoP qq = pp.linkPalabra;
                            while(i!=cont){
                                qq=qq.linkPalabra;
                                i=i+1;
                            }
                            System.out.println(qq.Palabras);
                            TraduccionTxt1.setText(qq.Palabras);
                        }else{
                           JOptionPane.showInputDialog(null, "No se encuentra el idioma al que se desea traducir"); 
                        }
                    }else{
                           JOptionPane.showInputDialog(null, "No se encuentra la palabra que se desea traducir"); 
                    }
                }else{
                    JOptionPane.showInputDialog(null, "No hay palabras registradas para ese idioma");
                }    
            }else{
                   JOptionPane.showInputDialog(null, "No se encuentra el idioma de origen de la palabra"); 
            }
        }
    }
    
    void EliminarPalabra(Nodo ptr, String word, String Idioma){
        Nodo p = ptr;
        while(p.linkIdiomas!=null && !p.Idiomas.equalsIgnoreCase(Idioma)){
            p = p.linkIdiomas;
        }
        if(p.linkPalabra!=null){
            NodoP anteQ = null;
            NodoP q = p.linkPalabra;
            while(q!=null && !q.Palabras.equals(word)){
                anteQ = q;
                q = q.linkPalabra;
            }
            if(q!=null && anteQ==null){
                p.linkPalabra=q.linkPalabra;
            }
        }

    }    
    
